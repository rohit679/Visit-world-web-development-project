        <li>The Nijo Castle, erected early in the 17th century for Ieyasu, the founder of the Tokugawa military dynasty, is one of the most splendid of all Japanese structures. Besides being a fine example of traditional Japanese architecture, it is famous for its painted decorations executed by Kano Tanyu and his assistants and for its beautiful garden designed by Kobori Enshu.</li>
